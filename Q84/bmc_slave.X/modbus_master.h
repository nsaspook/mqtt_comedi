	struct VM_type {
		uint32_t StartTime, TimeUsed;
		volatile uint32_t pacing, pwm_update, pwm_stop, fault_count, fault_ticks, fault_source, modbus_rx, modbus_tx;
		int32_t motor_speed;
		volatile bool fault_active, dmt_sosc_flag;
	};

	union PWMDC {
		uint16_t lpwm;
		uint8_t bpwm[2];
	};

	union MREG {
		int16_t value;
		char bytes[2];
	};

	union MREG32 {
		int32_t value;
		char bytes[4];
	};

	union MREG64 {
		int64_t value;
		char bytes[8];
	};

	typedef struct M_time_data { // ISR used, mainly for non-atomic mod problems
		uint32_t clock_500hz;
		uint32_t clock_500ahz;
		uint32_t clock_2hz;
	} M_time_data;

	typedef struct M_data { // ISR used, mainly for non-atomic mod problems
		uint8_t blink_lock : 1;
		uint8_t config : 1;
		uint8_t stable : 1;
		uint8_t boot_code : 1;
		uint8_t power_on : 1;
		uint8_t send_count, recv_count, pwm_volts;
		uint16_t error, crc_data, crc_calc;
		uint32_t crc_error;
		uint32_t to_error;
		uint32_t sends;
		volatile bool rx;
	} M_data;

	typedef struct C_data { // client state machine data
		uint8_t mcmd;
		comm_type cstate;
		cmd_type modbus_command;
		uint16_t req_length;
		int8_t trace;
		bool id_ok, passwd_ok, config_ok, data_ok, light_ok, serial_ok, version_ok, tm_ok;
		uint32_t data_count, data_prev;
		volatile M_data M;
	} C_data;

